"""
Γράψτε ένα πρόγραμμα που να τυπώνει
το παρακάτω σχήμα!

      *
     * *
    * * *
   * * * *
  * * * * *
 * * * * * *
* * * * * * *
Στη συνέχεια επεκτείνετε το πρόγραμμα ώστε
η βάση του τριγώνου να ορίζεται από τον χρήστη.
"""

# 1st solutiuon
n = int(input("Δώσε αριθμό γραμμών: "))
for i in range(1, n + 1):
    print(" "*(n-i), end="")
    print("* "*i)

# 2nd solutiuon
n = int(input('Δώσε αριθμό γραμμών: '))

for i in range(n):
    print((' '*(n-i-1))+ ('* ')*(i+1))
